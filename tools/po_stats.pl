#!/usr/bin/perl
use strict;
use warnings;

use Cwd                   qw{ cwd };
use File::Spec::Functions qw{ catfile catdir };
use File::Find::Rule;
use File::Basename        qw{ basename };

my $cwd       = cwd;
my $localedir = catdir ( $cwd, 'share', 'locale' );
my $pot_file  = catfile( $localedir, 'messages.pot' );

chdir $localedir;
my @po_files  = glob '*.po';

my $report = "Generated by $0\n on " . localtime() . "\n\n";
foreach my $po_file (@po_files) {
	system "msgcmp $po_file messages.pot 2> err";
	$report .= "------------------\n";
	$report .= basename($po_file) . "\n\n";
	if (open my $fh, '<', 'err') {
		local $/ = undef;
		$report .= <$fh>;
	}
	unlink 'err';
}
chdir $cwd;

open my $fh, '>', 'po_report.txt' or die;
print {$fh} $report;
